# Definir variables
nombre = "Juan"  # Tipo de dato: string
edad = 25  # Tipo de dato: integer
altura = 1.75  # Tipo de dato: float
es_estudiante = True  # Tipo de dato: boolean

# Imprimir variables
print("Nombre:", nombre)
print("Edad:", edad)
print("Altura:", altura)
print("Es estudiante:", es_estudiante)

# Tipos de datos de Python
tipo_nombre = type(nombre)
tipo_edad = type(edad)
tipo_altura = type(altura)
tipo_estudiante = type(es_estudiante)

print("Tipo de nombre:", tipo_nombre)
print("Tipo de edad:", tipo_edad)
print("Tipo de altura:", tipo_altura)
print("Tipo de es_estudiante:", tipo_estudiante)
