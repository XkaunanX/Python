# operadores.py
# Operadores aritméticos, lógicos, de comparación

# Operadores aritméticos
a = 10
b = 5

suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b
modulo = a % b
potencia = a ** b

print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("Módulo:", modulo)
print("Potencia:", potencia)

# Operadores lógicos
es_mayor = a > b
es_igual = a == b
es_diferente = a != b

print("¿a es mayor que b?", es_mayor)
print("¿a es igual a b?", es_igual)
print("¿a es diferente a b?", es_diferente)

# Operadores lógicos
y = True
o = False

and_logico = y and o
or_logico = y or o
not_logico = not y

print("AND:", and_logico)
print("OR:", or_logico)
print("NOT:", not_logico)
